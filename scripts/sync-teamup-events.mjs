import { createHash } from 'node:crypto';
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

const CALENDAR_KEY = 'ksakacjdxmrwoqtzym';
const KW_HAB_ORG_ID = '22222222-2222-2222-2222-222222222222';
const OUT = fileURLToPath(new URL('../db/teamup-events.generated.sql', import.meta.url));
const KW_HAB_CALENDAR_ID = 13210786;

function isoDay(date) {
  return date.toISOString().slice(0, 10);
}

function sql(value) {
  return value == null ? 'NULL' : `'${String(value).replaceAll("'", "''")}'`;
}

function uuidFor(value) {
  const hex = createHash('sha256').update(`kwhab-teamup:${value}`).digest('hex').slice(0, 32);
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-4${hex.slice(13, 16)}-a${hex.slice(17, 20)}-${hex.slice(20)}`;
}

function textFromHtml(html = '') {
  return html
    .replace(/<br\s*\/?\s*>/gi, '\n')
    .replace(/<[^>]*>/g, ' ')
    .replace(/&nbsp;|&#160;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/&quot;/gi, '"')
    .replace(/&#39;|&apos;/gi, "'")
    .replace(/\s+/g, ' ')
    .trim();
}

function categories(title, notes) {
  const value = `${title} ${notes}`.toLowerCase();
  const found = new Set(['social']);
  if (/art|paint|craft|zentangle|music|sing|karaoke|comedy|juke|creative/.test(value)) found.add('arts');
  if (/walk|hike|baseball|game|fitness|sport|bingo/.test(value)) found.add('sports');
  if (/bread|bake|baking|cookie|lunch|brunch|food|cafe|coffee|pizza|salad|jam|pickle/.test(value)) found.add('food');
  if (/walk|hike|park|outdoor|trail/.test(value)) found.add('outdoors');
  if (/transit|learn|class|workshop|diy|do it yourself/.test(value)) found.add('education');
  if (/health|wellness|fitness|walk/.test(value)) found.add('health');
  return [...found].slice(0, 3);
}

const today = new Date();
today.setHours(0, 0, 0, 0);
const end = new Date(today);
end.setDate(end.getDate() + 14);

const startDate = process.argv[2] || isoDay(today);
const endDate = process.argv[3] || isoDay(end);
const url = `https://teamup.com/${CALENDAR_KEY}/events?startDate=${startDate}&endDate=${endDate}`;
const response = await fetch(url);
if (!response.ok) throw new Error(`Teamup returned ${response.status} ${response.statusText}`);
const { events } = await response.json();

// These occurrences occupy the six stable demo IDs in seed.sql.
const seeded = new Set([
  'Make It Mondays - Zentangle with Duke|2026-07-13T13:00:00-04:00',
  'Games at the Hangout|2026-07-13T13:30:00-04:00',
  'Summer Baking - Cookie Dough Bites|2026-07-14T10:00:00-04:00',
  'Transit Tuesdays|2026-07-14T11:00:00-04:00',
  'Bring Along Sing Along|2026-07-15T11:00:00-04:00',
  'Walk With PCL|2026-07-15T12:45:00-04:00',
]);

const rows = events
  .filter((event) => !seeded.has(`${event.title}|${event.start_dt}`))
  .map((event) => {
    const notes = textFromHtml(event.notes);
    const description = notes || `${event.title}${event.location ? ` at ${event.location}` : ''}.`;
    const category = categories(event.title, notes);
    const isFree = event.subcalendar_ids?.includes(13104698) || /\bfree\b/i.test(`${event.title} ${notes}`);
    const isKwHab = event.subcalendar_ids?.includes(KW_HAB_CALENDAR_ID);
    const tags = isFree ? ['low_cost'] : [];
    return `  (${sql(uuidFor(event.id))}, ${sql(KW_HAB_ORG_ID)}, ${sql(event.title)}, ${sql(description)}, ${sql(description)}, ARRAY[${category.map(sql).join(',')}]::event_category[], ${sql(event.start_dt)}, ${sql(event.end_dt)}, ${sql(isFree ? 'free' : 'paid')}, NULL, NULL, NULL, NULL, ${sql(event.location || null)}, ARRAY[${tags.map(sql).join(',')}]::accommodation_tag[], 'not_yet_verified', 'form')${isKwHab ? '' : ' /* shared community calendar */'}`;
  });

const output = `-- GENERATED by scripts/sync-teamup-events.mjs. Do not hand-edit.\n-- Public Teamup occurrences from ${startDate} through ${endDate}.\nINSERT INTO events\n  (id, org_id, title, description, plain_language_description, category, date_start, date_end,\n   cost, cost_amount, age_group, location_lat, location_lng, location_address,\n   accommodation_tags, accessibility_badge_state, created_via) VALUES\n${rows.join(',\n')}\nON CONFLICT (id) DO NOTHING;\n`;

await writeFile(OUT, output);
console.log(`Wrote ${rows.length} Teamup event occurrences to ${OUT}`);
